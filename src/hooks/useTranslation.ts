export const useTranslation = () => {
    const { currentUser } = useAppContext();
    const lang = currentUser?.preferredLanguage || 'English';

    const t = (key: string) => {
        return translations[lang]?.[key] || translations['English']?.[key] || key;
    };

    return { t, lang };
};
